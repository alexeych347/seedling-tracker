/* ─── Helpers ─────────────────────────────────────────────────────────────── */

DB.getVariety = function(typeKey, varietyKey) {
  const type = DB[typeKey];
  if (!type) return null;
  return type.varieties[varietyKey] || Object.values(type.varieties)[0];
};

DB.getVarietyList = function(typeKey) {
  const type = DB[typeKey];
  if (!type) return [];
  return Object.entries(type.varieties).map(([key, v]) => ({ key, name: v.name, fullName: v.fullName, emoji: v.emoji }));
};

DB.getAllTypes = function() {
  return Object.entries(DB)
    .filter(([, v]) => typeof v === 'object' && v.varieties)
    .map(([key, v]) => ({ key, name: v.name, emoji: v.emoji, accentColor: v.accentColor }));
};
